export function sum(a, b) {
    return a + b;
}

export function multiply(a, b) {
    return a * b;
}

export function divide(a, b) {
    return a / b;
}

export function pow(a, b) {
    return a ** b;
}

export function sqrt(a) {
    return Math.sqrt(a);
}

export function log(a) {
    return Math.log(a);
}